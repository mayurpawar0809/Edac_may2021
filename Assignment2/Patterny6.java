import java.util.*;
class Patterny6
{
public static void main(String args[])
{
for(int i=1;i<=5;i++)//row
{
for(int j=1;j<=i;j++)//column sp
{
System.out.print(" " );
}
{
for(int k=5;k>=i;k--)//column str 1
{
System.out.print("*" );
}
{
for(int l=4;l>=i;l--)//column str 2
{
System.out.print("*" );
}
System.out.println();
}
}
}
}
}